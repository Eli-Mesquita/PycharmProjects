import math
ang = float(input('Digite o ângulo:'))
sen = math.sin(math.radians(ang))
cos = math.cos(math.radians(ang))
tan = math.tan(math.radians(ang))
print('O seno de {:.0f}º é {:.2f}'.format(ang, sen))
print('O cosseno de {:.0f}º é {:.2f}'. format(ang, cos))
print('A tangente de {:.0f}º é {:.2f}'.format(ang, tan))











